typedef std::vector<unsigned int> State_t;

struct move {
  State_t state;
  std::string move_string;
  unsigned int move_number;
  unsigned int start_pos;
  int mover_idx;
  unsigned int end_pos;
  int end_idx;  // -1 if not attack
  bool attack;
  bool pawn_promotion;
  bool terminal;
  int value;
  unsigned long long int hash;

 move & operator=(const move & other) {
   state = other.state;
   move_string = other.move_string;
   start_pos = other.start_pos;
   mover_idx = other.mover_idx;
   end_pos = other.end_pos;
   end_idx = other.end_idx;
   attack = other.attack;
   pawn_promotion = other.pawn_promotion;
   terminal = other.terminal;
   value = other.value;
   hash = other.hash;
   return *this;
 }

/**
   * This makes it so the move can be used in std::min, std::max
   * std::sort, etc.
   *
   * @param a
   * @param b
   * @return
   */
  friend bool operator<(const move &a, const move &b) {
    return a.value < b.value;
  }

  friend bool operator>(const move &a, const move &b) {
    return a.value < b.value;
  }

  /**
   * This will be helpful when comparing moves from hash table.
   *
   * @param other
   * @return bool
   */
  bool operator==(const move &other) {
    return this->hash == other.hash;
  }

  /**
   * This will be helpful when comparing moves from hash table.
   *
   * @param other
   * @return bool
   */
  bool operator!=(const move &other) {
    return this->hash != other.hash;
  }

  /**
   * Negates the value. Handy for negamax function.
   *
   * @return
   */
  move operator-() const {
    move result;
    result.state = state;
    result.move_string = move_string;
    result.move_number = move_number;
    result.start_pos = start_pos;
    result.mover_idx = mover_idx;
    result.end_pos = end_pos;
    result.end_idx = end_idx;
    result.attack = attack;
    result.pawn_promotion = pawn_promotion;
    result.terminal = terminal;
    result.value = -value;  // This is the negation
    result.hash = hash;
    return result;
  }
};

